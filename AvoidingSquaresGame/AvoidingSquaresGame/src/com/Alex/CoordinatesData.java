package com.Alex;

public class CoordinatesData {

    public Integer X;
    public Integer Y;
    private Integer W;
    private Integer H;
    private Integer R;

    public Integer getX() {
        return X;
    }

    public void setX(Integer x) {
        X = x;
    }


    public void setY(Integer y) {
        Y = y;
    }

    public Integer getW() {
        return W;
    }

    public void setW(Integer w) {
        W = w;
    }


    public void setH(Integer h) {
        H = h;
    }

    public Integer getR() {
        return R;
    }

    public void setR(Integer r) {
        R = r;
    }
}
